param(
    [string] $config = $(Throw 'config is required!'),

    [string] $log = $(Throw 'log is required!'))

$errorActionPreference = 'Stop'

try
{
    $scriptDir = Split-Path -Path $MyInvocation.MyCommand.Path -Parent
    . (Join-Path $scriptDir 'Common-Configuration.ps1')

    $settings = @{}

    # Decode all config values
    Write-Log -objectToLog ("Input configuration string: {0}" -f $config) -logFile $log
    $decodedConfig = [System.Text.Encoding]::UTF8.GetString([System.Convert]::FromBase64String($config))

    $settings = ConvertFrom-Json $decodedConfig
    Write-Log -objectToLog ("Decoded configuration string: {0}" -f $settings) -logFile $log

    # Read all decoded config values
    $targetPackagesFolder = $settings.PackagesDropLocation
    $targetSDKFolder = (Split-Path $targetPackagesFolder -Parent)

    Remove-Item -Path $targetPackagesFolder -Force -Recurse -ErrorAction SilentlyContinue

    # Copy RetailSDK packages and scripts to correct location
    $retailSDKRootFolder = (Split-Path $scriptDir -Parent)
    
	$RetailTenantUpdateToolName = 'RetailTenantUpdateTool'
	Copy-Files -SourceDirPath (Join-Path $retailSDKRootFolder 'Code') -DestinationDirPath $targetSDKFolder
	$toolDestinationDirPath = (Join-Path $targetSDKFolder 'Tools')
    Copy-Files -SourceDirPath (Join-Path $retailSDKRootFolder 'Scripts') -DestinationDirPath $toolDestinationDirPath -FilesToCopy "$RetailTenantUpdateToolName.ps1"

	Create-Shortut -ShortcutPath ("{0}\Desktop\{1}.lnk" -f $Home,$RetailTenantUpdateToolName) -ShortcutTargetPath "PowerShell.exe" -ShortcutArguments ("-File {0}\{1}.ps1" -f $toolDestinationDirPath,$RetailTenantUpdateToolName)

    # Add installation directory to registry.
    $productVersion = Get-ProductVersionMajorMinor
    $retailSdkRegistryKey = "HKLM:\Software\Microsoft\Dynamics\{0}\RetailSDK" -f $productVersion
    [void](New-Item $retailSdkRegistryKey -Force)
    New-ItemProperty -Path $retailSdkRegistryKey -Name 'InstallationDir' -PropertyType 'String' -Value $targetSDKFolder -Force
}
catch
{
    Write-Log ($global:error[0] | Format-List * -Force | Out-String -Width 4096) -logFile $log
    $ScriptLine = "{0}{1}" -f $MyInvocation.MyCommand.Path.ToString(), [System.Environment]::NewLine

    # Set a non-zero unique exit code for RetailSDK deploy failure
    $exitCode = 24742
    Write-Log ("Executed:{0}$ScriptLine{0}Exiting with error code $exitCode." -f [System.Environment]::NewLine) -logFile $log
    exit $exitCode
}


