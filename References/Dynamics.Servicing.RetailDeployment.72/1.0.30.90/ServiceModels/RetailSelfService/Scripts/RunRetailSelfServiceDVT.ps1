param(
    [string]$serviceModelXml,
    [string]$config,
    [string]$log = $(Throw 'log parameter required')
)

$ErrorActionPreference = 'Stop'
$scriptDir = Split-Path -parent $MyInvocation.MyCommand.Path

try
{
    if (!$config -and !$serviceModelXml) 
    {
        Throw 'Atleast one of config or serviceModelXml parameters required.'
    }

    $dvtHelperModuleName = 'CommonDVTHelperModule.psm1'
    Import-Module (Join-Path -Path $scriptDir -ChildPath $dvtHelperModuleName) -DisableNameChecking

    # Script Variables
    [string]$parentDir = Split-Path -Path $PSCommandPath -Parent
    [string]$dvtScript = (Join-Path -Path $parentDir -ChildPath 'ValidateRetailSelfService.ps1')
    [string]$serviceModelDirName = 'RunRetailSelfServiceDVT'
    [string]$dvtHelperScript = (Join-Path -Path $scriptDir -ChildPath $dvtHelperModuleName)
    [string]$selfServiceConfigurationModule = (Join-Path -Path $scriptDir -ChildPath 'SelfServiceConfiguration.psm1')
    [string]$commonConfigurationScript = (Join-Path -Path $scriptDir -ChildPath 'Common-Configuration.ps1')
    [string]$commonWebScript = (Join-Path -Path $scriptDir -ChildPath 'Common-Web.ps1')

    # Array to hold the list of all helper scripts
    [array]$dvtHelperScripts = @($dvtHelperScript, $selfServiceConfigurationModule, $commonConfigurationScript, $commonWebScript)

    # Main Execution
    Run-NonWebServiceBasedServiceModelDVT -serviceModelXml $serviceModelXml `
                                          -config $config `
                                          -serviceModelDirName $serviceModelDirName `
                                          -dvtScript $dvtScript `
                                          -dvthelperScripts $dvtHelperScripts `
                                          -log $log
}
catch
{
    Handle-DvtError -errorObject $_ -log $log
}


