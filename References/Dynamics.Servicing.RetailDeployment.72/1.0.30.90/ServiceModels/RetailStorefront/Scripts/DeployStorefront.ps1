param (
     [string]$TopologyXmlFilePath = $(Throw "The topology XML file name is required!"),
     [string]$SettingsXmlFilePath = $(Throw "The settings XML file name is required!"),
     [System.Management.Automation.PSCredential[]]$Credentials
)

$ErrorActionPreference = "Stop"

try
{
    $ScriptDir = Split-Path -parent $MyInvocation.MyCommand.Path;
    . (Join-Path $ScriptDir 'Common-Configuration.ps1')
    . (Join-Path $ScriptDir 'Common-Web.ps1')
	
	[xml]$TopologyXml = Expand-VariablesFromSettingsFile $TopologyXmlFilePath $SettingsXmlFilePath
	$storefrontConfig = Get-RetailServerConfigFromXPath $TopologyXml "//Settings/StorefrontInstance"
	
	Log-TimedMessage "Starting prerequisite checks."

    if(-not (Verify-ApplicationPoolCredentials -WebSiteConfig $storefrontConfig.WebSite -Credentials $Credentials))
    {
        # issue detected in dot source file. Assuming error/warning was printed already, just exiting. This will exit the main script.
        exit ${global:PrerequisiteFailureReturnCode};
    }
	
	Install-WebApplication -WebSiteConfig $storefrontConfig.WebSite -Credentials $Credentials

    $productVersion = Get-ProductVersionMajorMinor
    $RetailComponentRegistryKey = "HKLM:\Software\Microsoft\Dynamics\{0}\RetailStorefront" -f $productVersion

    # Add the registry key
    Add-RegistryEntry -WebSiteConfig $storefrontConfig.WebSite -RetailComponentRegistryKey $RetailComponentRegistryKey
	
	Log-TimedMessage "Finished Retail Storefront installation successfully."
}
catch
{
    Log-Error ($global:error[0] | Format-List * -f | Out-String)
    $ScriptLine = "{0}{1}" -f $MyInvocation.MyCommand.Path.ToString(), [System.Environment]::NewLine
    $PSBoundParameters.Keys | % { $ScriptLine += "Parameter: {0} Value: {1}{2}" -f $_.ToString(), $PSBoundParameters[$_.ToString()], [System.Environment]::NewLine}
    $exitCode = 2
    Log-TimedMessage ("Executed:{0}$ScriptLine{0}Exiting with error code $exitCode." -f [System.Environment]::NewLine)
    exit $exitCode
}
