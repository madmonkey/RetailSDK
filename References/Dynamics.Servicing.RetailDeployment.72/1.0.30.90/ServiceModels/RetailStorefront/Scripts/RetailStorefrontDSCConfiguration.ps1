$global:username=$env:USERNAME

& "$grandparentdir\Scripts\CreateRetailStorefrontDSCConfiguration.ps1"  -PackageDirectory $packageDir -ServiceModelXml $global:decodedservicemodelxml -OutputPath $outputpath -Verbose *>&1 | Tee-Object $log


