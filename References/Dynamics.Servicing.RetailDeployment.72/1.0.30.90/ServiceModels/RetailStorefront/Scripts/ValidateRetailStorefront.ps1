# Validate Retail Storefront deployment

Param(
   [string]$inputXml,
   [string]$log = (Join-Path -Path $env:SystemDrive -ChildPath 'ValidateRetailStorefront.log')
)
$ErrorActionPreference = 'Stop'
$ScriptDir = Split-Path -parent $MyInvocation.MyCommand.Path

# Script Variables
[string]$parentDir = Split-Path -parent $PSCommandPath
[string]$logDir = Split-Path -Path $log -Parent
[string]$logFile = (Join-Path -Path $logDir -ChildPath 'Debug_ValidateRetailStorefront.log')

try
{
	Log-TimedMessage -logMessage "Running Retail Storefront DVT Script" -log $logFile
	
	# Parameters from Input XML
	if(Test-Path -Path $inputXml)
	{
		Log-TimedMessage -logMessage "Parsing the xml for parameters/settings" -log $logFile
		[xml]$dvtParams = Get-Content -Path $inputXml
		[string]$serviceName = $dvtParams.DVTParameters.ServiceName
		[string]$appPoolName = $dvtParams.DVTParameters.AppPoolName
		[string]$appPoolState = $dvtParams.DVTParameters.AppPoolState
		[string]$xmlOutputPath = $dvtParams.DVTParameters.OutputPath
	}
	else
	{
		Throw "Unable to parse settings from service model. Xml doesn't exist at: $inputXml"
	}

	# Additional Parameters
	[string]$collectorName = 'RunRetailStorefront.DVT'
	[string]$collectorType = 'PowerShellCollector'
	[string]$targetName = $collectorName
	[string]$serviceModelName = 'RetailStorefront'

	# Create the container if it doesnt exist.
	if(-not (Test-Path -Path $xmlOutputPath))
	{
		New-Item -Path $xmlOutputPath -Type Container | Out-Null
	}

	[string]$xmlChildPath = "{0}_RunRetailStorefrontDVTResults.xml" -f [System.DateTime]::Now.ToFileTimeUtc()
	[string]$xmlFilePath = (Join-Path -Path $xmlOutputPath -ChildPath $xmlChildPath)

	# Execute Apppool DVT
	Execute-ServiceModelAppPoolDVT -serviceModelName $serviceModelName `
								   -AppPoolName $appPoolName `
								   -AppPoolState $appPoolState `
								   -logFile $logFile `
								   -xmlFilePath $xmlFilePath
}
catch
{
    Handle-DvtError -errorObject $_ -log $log
}


