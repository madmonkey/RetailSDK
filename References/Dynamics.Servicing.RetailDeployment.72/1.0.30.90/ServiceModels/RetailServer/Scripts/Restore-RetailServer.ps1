$ErrorActionPreference = 'Stop'

try
{	
	#Note: $RunbookBackupFolder and $PSScriptRoot variables are populated directly from runbook.
	Import-Module "$PSScriptRoot\CommonRollbackUtilities.psm1" -Force -DisableNameChecking
	
	Restore-WebSite -Name 'RetailServer' -BackupFolder $RunbookBackupFolder
	
	# Register perf counters from restored instrumentation assembly
	$webroot = Get-WebSitePhysicalPath -Name 'RetailServer'
	
	$registerPerfCounterScriptPath = "$PSScriptRoot\Register-PerfCounters.ps1"
