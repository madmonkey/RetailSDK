param
(
    [string]$ServiceModelXml = $(Throw 'servicemodelxml is required!'),
    [string]$log = $(Throw 'log is required!'),
    [string]$config = $(Throw 'config is required!')
)

try
{
    $scriptDir = Split-Path -Path $MyInvocation.MyCommand.Path -Parent
    $AxInstallationInfoDllPath = Join-Path -Path $scriptDir -ChildPath 'Microsoft.Dynamics.AX.AXInstallationInfo.dll'
    Import-Module (Join-Path -Path $scriptDir -ChildPath 'ServiceModelUtilities.psm1') -DisableNameChecking

    $serviceModelName = 'RetailServer'
    Write-Log ('Executing {0} website uninstallation script.' -f $serviceModelName) -logFile $log

    Uninstall-RetailWebService -serviceModelName $serviceModelName `
                               -encodedConfigString $config `
                               -pathToAxInstallationInfoDll $AxInstallationInfoDllPath `
                               -logFile $log

    Write-Log ('Finished executing {0} website uninstallation script.' -f $serviceModelName) -logFile $log
}
catch
{
