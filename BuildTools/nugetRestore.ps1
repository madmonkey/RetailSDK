$ErrorActionPreference = "Stop"

$pathToNuget = "$PSScriptRoot/../References/nuget.exe"
# This script will automatically download nuget.exe to the References folder of the SDK.

if (-not (Test-Path $pathToNuget))
{
    try 
    {
        Invoke-WebRequest -uri "https://dist.nuget.org/win-x86-commandline/latest/nuget.exe" -OutFile "$pathToNuget"
    }
    catch 
    {
        "Nuget.exe failed to download. Try running the build again. If this problem persists, you can download nuget.exe manually from nuget.org/downloads and drop it into the References folder."
        break
    }
}

$srcRoot = Resolve-Path "$PSScriptRoot\.."

Get-ChildItem $srcRoot\* -Recurse -Filter packages.config | Where { ! $_.PSIsContainer } | ForEach-Object {
    Write-Host "Restore $($_.FullName)"
    & $pathToNuget restore $_.FullName
    if ($LASTEXITCODE -gt 0)
    {
        throw "Package restore failed for $($_.FullName)."
    }
}

Get-ChildItem $srcRoot\* -Recurse -Filter *.sln | ForEach-Object {
    Write-Host "Restore $($_.FullName)"
    & $pathToNuget restore $_.FullName
    if ($LASTEXITCODE -gt 0)
    {
        throw "Package restore failed for $($_.FullName)."
    }
}

